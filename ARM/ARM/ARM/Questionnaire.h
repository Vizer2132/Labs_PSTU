using namespace std;

class Questionnaire {
private:
	string name, surname;
	Date birthsdayDate;
	int age;
	
	Phone parentsPhoneNumber;

public:
	Questionnaire(string n, string sn, Date d, Phone p) {
		name = n;
		surname = sn;
		birthsdayDate = d;
		age = Date::findAge(d);
		parentsPhoneNumber = p;

	}



};